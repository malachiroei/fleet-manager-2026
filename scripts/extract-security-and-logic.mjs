/**
 * Extract CREATE OR REPLACE FUNCTION, CREATE TRIGGER, CREATE POLICY
 * from supabase/full_schema_smart.sql in source order (by position in file).
 * Output: supabase/full_schema_security_and_logic.sql
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const srcPath = resolve(root, 'supabase/full_schema_smart.sql');
const outPath = resolve(root, 'supabase/full_schema_security_and_logic.sql');

const sql = readFileSync(srcPath, 'utf8');
const n = sql.length;

/** Read until semicolon at depth 0 outside quotes / dollar quotes */
function readStatementToSemicolon(str, start) {
  const len = str.length;
  let i = start;
  let depth = 0;
  let inSingle = false;
  let inDouble = false;
  let dollarTag = null;

  while (i < len) {
    const c = str[i];

    if (dollarTag !== null) {
      if (dollarTag === '') {
        if (c === '$' && str[i + 1] === '$') {
          dollarTag = null;
          i += 2;
          continue;
        }
      } else {
        const close = `$${dollarTag}$`;
        if (str.slice(i, i + close.length) === close) {
          i += close.length;
          dollarTag = null;
          continue;
        }
      }
      i++;
      continue;
    }

    if (inSingle) {
      if (c === "'" && str[i + 1] === "'") {
        i += 2;
        continue;
      }
      if (c === '\\' && i + 1 < len) {
        i += 2;
        continue;
      }
      if (c === "'") inSingle = false;
      i++;
      continue;
    }
    if (inDouble) {
      if (c === '"' && str[i + 1] === '"') {
        i += 2;
        continue;
      }
      if (c === '"') inDouble = false;
      i++;
      continue;
    }

    if (c === "'") {
      inSingle = true;
      i++;
      continue;
    }
    if (c === '"') {
      inDouble = true;
      i++;
      continue;
    }

    if (c === '$') {
      const rest = str.slice(i + 1);
      const m = rest.match(/^([A-Za-z0-9_]*)\$/);
      if (m) {
        dollarTag = m[1] ?? '';
        i += 1 + m[0].length;
        continue;
      }
    }

    if (c === '(') depth++;
    if (c === ')') depth = Math.max(0, depth - 1);

    if (c === ';' && depth === 0) {
      return { end: i + 1, text: str.slice(start, i + 1).trim() };
    }
    i++;
  }
  return null;
}

function lineStart(str, pos) {
  let s = pos;
  while (s > 0 && str[s - 1] !== '\n') s--;
  return s;
}

/** Skip match if it's on a line that starts with -- (after optional whitespace) */
function isCommentOnlyLine(str, matchIndex) {
  const ls = lineStart(str, matchIndex);
  const nl = str.indexOf('\n', matchIndex);
  const line = str.slice(ls, nl === -1 ? str.length : nl);
  return /^\s*--/.test(line);
}

const patterns = [
  { re: /\bCREATE OR REPLACE FUNCTION\b/gi, name: 'CREATE OR REPLACE FUNCTION' },
  { re: /\bCREATE TRIGGER\b|\bcreate trigger\b/g, name: 'CREATE TRIGGER' },
  { re: /\bCREATE POLICY\b/gi, name: 'CREATE POLICY' },
];

const matches = [];
for (const { re } of patterns) {
  re.lastIndex = 0;
  let m;
  while ((m = re.exec(sql)) !== null) {
    if (isCommentOnlyLine(sql, m.index)) continue;
    matches.push({ index: m.index });
  }
}

matches.sort((a, b) => a.index - b.index);

const out = [];
let cursor = 0;
for (const { index } of matches) {
  if (index < cursor) continue;
  const stmt = readStatementToSemicolon(sql, index);
  if (!stmt) continue;
  const t = stmt.text;
  if (!/^\s*CREATE\s+(?:OR\s+REPLACE\s+)?FUNCTION\b/i.test(t) && !/^\s*CREATE\s+TRIGGER\b/i.test(t) && !/^\s*CREATE\s+POLICY\b/i.test(t)) {
    cursor = stmt.end;
    continue;
  }
  out.push(t);
  cursor = stmt.end;
}

const header = `-- full_schema_security_and_logic.sql
-- Extracted from full_schema_smart.sql: CREATE OR REPLACE FUNCTION, CREATE TRIGGER, CREATE POLICY (file order).
-- Generated by scripts/extract-security-and-logic.mjs

`;

writeFileSync(outPath, header + out.join('\n\n') + '\n', 'utf8');
console.log('Wrote', out.length, 'statements ->', outPath);
