/**
 * Builds supabase/full_schema_smart.sql from full_schema.sql
 * (idempotent-friendly DDL). Run: node scripts/build-full-schema-smart.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const src = readFileSync(resolve(root, 'supabase/full_schema.sql'), 'utf8');

let out = src;

// CREATE INDEX ON table(...) without index name — IF NOT EXISTS requires a name in PostgreSQL
let anonIdx = 0;
out = out.replace(/^CREATE INDEX ON\s+(\S+)\s*\(([^)]+)\)\s*;/gm, (_m, table, cols) => {
  anonIdx += 1;
  const safe = String(table).replace(/[^a-zA-Z0-9_]/g, '_');
  const name = `idx_smart_anon_${anonIdx}_${safe}`;
  return `CREATE INDEX IF NOT EXISTS ${name} ON ${table} (${cols});`;
});

// CREATE TABLE → IF NOT EXISTS (skip when already present)
out = out.replace(/(^|\n)CREATE TABLE\s+(?!IF NOT EXISTS)/gm, '$1CREATE TABLE IF NOT EXISTS ');

// CREATE UNIQUE INDEX → IF NOT EXISTS (before plain CREATE INDEX)
out = out.replace(/(^|\n)CREATE UNIQUE INDEX\s+(?!IF NOT EXISTS)/gm, '$1CREATE UNIQUE INDEX IF NOT EXISTS ');

// CREATE INDEX → IF NOT EXISTS (named indexes only; anonymous already handled above)
out = out.replace(/(^|\n)CREATE INDEX\s+(?!IF NOT EXISTS)/gm, '$1CREATE INDEX IF NOT EXISTS ');

// CREATE FUNCTION → CREATE OR REPLACE FUNCTION (skip OR REPLACE)
out = out.replace(/CREATE\s+(?!OR\s+REPLACE\s+)FUNCTION\b/gi, 'CREATE OR REPLACE FUNCTION');

// CREATE TYPE ... AS ENUM (...); → DO block with pg_type check
out = out.replace(
  /CREATE TYPE\s+(?:(\w+)\.)?(\w+)\s+AS\s+ENUM\s*\(([^)]+)\)\s*;/g,
  (full, schema, typeName, enumBody) => {
    const nsp = schema || 'public';
    const fq = schema ? `${schema}.${typeName}` : `public.${typeName}`;
    return `DO $smart$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_catalog.pg_type t
    JOIN pg_catalog.pg_namespace n ON n.oid = t.typnamespace
    WHERE n.nspname = '${nsp}' AND t.typname = '${typeName}'
  ) THEN
    CREATE TYPE ${fq} AS ENUM (${enumBody});
  END IF;
END $smart$;`;
  },
);

const header = `-- full_schema_smart.sql
-- Derived from full_schema.sql: CREATE TABLE/INDEX idempotency, conditional CREATE TYPE, CREATE OR REPLACE FUNCTION.
-- Generated by scripts/build-full-schema-smart.mjs — re-run after regenerating full_schema.sql.

`;

writeFileSync(resolve(root, 'supabase/full_schema_smart.sql'), header + out, 'utf8');
console.log('Wrote supabase/full_schema_smart.sql');
